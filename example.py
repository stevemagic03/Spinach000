# Simple Python program with input and output

# Get user input
name = input("What is your name? ")
age = input("How old are you? ")

# Process the input
age = int(age)
next_year_age = age + 1

# Output the results
print("\n--- Output ---")
print(f"Hello, {name}!")
print(f"You are {age} years old.")
print(f"Next year, you will be {next_year_age} years old.")
 